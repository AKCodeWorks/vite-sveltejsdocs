const formatTypeObject = (value: string): string => {
	const source = value.trim();
	if (!source) return '';

	const indentUnit = '  ';
	let out = '';
	let depth = 0;
	let inSingleQuote = false;
	let inDoubleQuote = false;
	let escaped = false;

	const isWhitespace = (ch: string): boolean => ch === ' ' || ch === '\\n' || ch === '\\r' || ch === '\\t';
	const appendIndent = (count: number): void => {
		if (count <= 0) return;
		out += indentUnit.repeat(count);
	};
	const nextNonWhitespace = (start: number): string => {
		for (let i = start; i < source.length; i += 1) {
			const ch = source[i];
			if (!isWhitespace(ch)) return ch;
		}
		return '';
	};

	for (let i = 0; i < source.length; i += 1) {
		const ch = source[i];

		if (inSingleQuote || inDoubleQuote) {
			out += ch;
			if (escaped) {
				escaped = false;
				continue;
			}
			if (ch === '\\\\') {
				escaped = true;
				continue;
			}
			if (inSingleQuote && ch === "'") {
				inSingleQuote = false;
			} else if (inDoubleQuote && ch === '"') {
				inDoubleQuote = false;
			}
			continue;
		}

		if (ch === "'") {
			inSingleQuote = true;
			out += ch;
			continue;
		}

		if (ch === '"') {
			inDoubleQuote = true;
			out += ch;
			continue;
		}

		if (collapseJson) {
			if (isWhitespace(ch)) {
				if (out.length > 0 && out[out.length - 1] !== ' ') out += ' ';
				continue;
			}
			out += ch;
			continue;
		}

		if (isWhitespace(ch)) {
			if (out.endsWith('\\n')) continue;
			if (!out.endsWith(' ')) out += ' ';
			continue;
		}

		if (ch === '{' || ch === '[') {
			out += ch;
			depth += 1;
			const closing = ch === '{' ? '}' : ']';
			if (nextNonWhitespace(i + 1) !== closing) {
				out += '\\n';
				appendIndent(depth);
			}
			continue;
		}

		if (ch === '}' || ch === ']') {
			depth = Math.max(0, depth - 1);
			while (out.endsWith(' ')) out = out.slice(0, -1);
			if (!out.endsWith('\\n') && out.length > 0) {
				out += '\\n';
			}
			appendIndent(depth);
			out += ch;
			continue;
		}

		if (ch === ',') {
			out += ',\\n';
			appendIndent(depth);
			continue;
		}

		if (ch === ':') {
			while (out.endsWith(' ')) out = out.slice(0, -1);
			out += ': ';
			continue;
		}

		if (ch === '|') {
			while (out.endsWith(' ')) out = out.slice(0, -1);
			out += ' | ';
			continue;
		}

		out += ch;
	}

	return out.trim();
};

