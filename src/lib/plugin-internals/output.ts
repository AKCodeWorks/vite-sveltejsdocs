import type {
	AutoDocEntry,
	AuthorProfile,
	DocHistoryVersionRecord,
	JsAutoDocsIndexOptions
} from './types.js';
type VersionedDocEntry = AutoDocEntry & {
	lastModified: string;
	versionHistory: string[];
};

/**
 * @auto-doc
 * @title Generated Module Emitter
 * @group Output
 * Produces the generated TypeScript module consumed by docs routes.
 * @param docs Parsed docs entries to serialize.
 * @returns TypeScript source code for `src/routes/docs/generated.ts`.
 */
export const createGeneratedModule = (
	docs: VersionedDocEntry[],
	authors: Record<string, AuthorProfile> = {},
	historyByDocId: Record<string, { versions: DocHistoryVersionRecord[] }> = {}
): string => {
	const payload = JSON.stringify(docs, null, 2);
	const authorsPayload = JSON.stringify(authors, null, 2);
	const historyPayload = JSON.stringify(
		Object.fromEntries(
			Object.entries(historyByDocId).map(([docId, record]) => [
				docId,
				(record?.versions ?? []).map((entry) => ({
					version: entry.version,
					changedAt: entry.changedAt,
					snapshot: {
						...entry.snapshot,
						jsdocRaw: entry.snapshot.jsdocRaw ?? '',
						response: entry.snapshot.response ?? '',
						headers: entry.snapshot.headers ?? '',
						body: entry.snapshot.body ?? '',
						fetchUrl: entry.snapshot.fetchUrl ?? ''
					}
				}))
			])
		),
		null,
		2
	);
	return `// This file is generated by jsAutoDocs. Do not edit manually.\n\nexport type GeneratedDocTag = {\n\tname: string;\n\tvalue: string;\n};\n\nexport type GeneratedDocEntry = {\n\tid: string;\n\tslug: string;\n\tgroup: string;\n\ttitle: string;\n\tsummary: string;\n\tdescription: string;\n\tjsdocRaw: string;\n\tsignature: string;\n\tfilePath: string;\n\tline: number;\n\tdisplayPath: string;\n\tparams: GeneratedDocTag[];\n\treturns: string;\n\tresponse: string;\n\theaders: string;\n\tbody: string;\n\tfetchUrl: string;\n\texamples: string[];\n\ttags: GeneratedDocTag[];\n\tlastModified: string;\n\tversionHistory: string[];\n};\n\nexport type GeneratedDocSnapshot = Pick<\n\tGeneratedDocEntry,\n\t'group' | 'title' | 'summary' | 'description' | 'jsdocRaw' | 'signature' | 'displayPath' | 'params' | 'returns' | 'response' | 'headers' | 'body' | 'fetchUrl' | 'examples' | 'tags'\n>;\n\nexport type GeneratedDocVersionRecord = {\n\tversion: string;\n\tchangedAt: string;\n\tsnapshot: GeneratedDocSnapshot;\n};\n\nexport type GeneratedAuthorProfile = {\n\tname: string;\n\ttitle?: string;\n\tusername?: string;\n\tbio?: string;\n\timageUrl?: string;\n\temail?: string;\n\taddress?: string;\n\tphone?: Partial<Record<'mobile' | 'office' | 'home' | 'other', string>>;\n\tlinks?: Record<string, string>;\n};\n\nexport const generatedAt = ${JSON.stringify(new Date().toISOString())};\n\nexport const docs: GeneratedDocEntry[] = ${payload};\n\nexport const authors: Record<string, GeneratedAuthorProfile> = ${authorsPayload};\n\nexport const history: Record<string, GeneratedDocVersionRecord[]> = ${historyPayload};\n`;
};

